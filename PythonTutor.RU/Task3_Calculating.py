# Задача «Первая цифра после точки»
"""
Дано положительное действительное число X. Выведите его первую цифру после десятичной точки.
"""

x = float(input())
k = int(x)
if k == 0:
    y = x * 10
    z = int(y)
    print(z)
if k > 0:
    print(int(10 * (x - k)))

