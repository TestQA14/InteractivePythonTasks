# Задача «Стоимость покупки»
"""
Пирожок в столовой стоит a рублей и b копеек. Определите, сколько рублей и копеек нужно 
заплатить за n пирожков. Программа получает на вход три числа: a, b, n, и должна вывести два 
числа: стоимость покупки в рублях и копейках.
"""

a = int(input())
b = int(input())
n = int(input())
r = a * n
k = b * n
if k >= 100:
    r = r + k // 100
    k = k % 100
print(r, k)

# Задача «Разность времен»
"""
Даны значения двух моментов времени, принадлежащих одним и тем же суткам: часы, минуты и секунды 
для каждого из моментов времени. Известно, что второй момент времени наступил не раньше первого. 
Определите, сколько секунд прошло между двумя моментами времени.

Программа на вход получает три целых числа: часы, минуты, секунды, задающие первый момент времени 
и три целых числа, задающих второй момент времени.

Выведите число секунд между этими моментами времени.
"""

h1 = int(input())
m1 = int(input())
s1 = int(input())
h2 = int(input())
m2 = int(input())
s2 = int(input())
h = (h2 - h1) * 3600
m = (m2 - m1) * 60
s = s2 - s1
print(h + m + s)

# Задача «Улитка»
"""
Улитка ползет по вертикальному шесту высотой h метров, поднимаясь за день на a метров, а за ночь спускаясь 
на b метров. На какой день улитка доползет до вершины шеста?

Программа получает на вход натуральные числа h, a, b.

Программа должна вывести одно натуральное число. Гарантируется, что a>b.
"""

h = int(input())
a = int(input())
b = int(input())
d = 1 + math.ceil((h - a) / (a - b))
print(d)

